package Day06;

public class Member {
	// * 회원[Member] 클래스 설계
	
	
	// 1. 필드 [ 회원정보를 저장하는 메모리 ]
	String id; // 선언만 했기 때문에 초기값은 null 할당
	String password;
	String name;
	String phone;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	// 2. 생성자
	
	// 3. 메소드
	
	
}
